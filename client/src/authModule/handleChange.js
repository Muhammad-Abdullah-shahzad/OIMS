const handleChange = ({e,setValidationErrors,setFirstName,setLastName,setEmail,setConfirmPassword,validationErrors,setPassword}) => {
    const { name, value } = e.target;
    // Clear specific validation error as user types
    if (validationErrors[name]) {
      setValidationErrors(prev => {
        const newErrors = { ...prev };
        delete newErrors[name];
        return newErrors;
      });
    }

    switch (name) {
      case 'firstName':
        setFirstName(value);
        break;
      case 'lastName':
        setLastName(value);
        break;
      case 'email':
        setEmail(value);
        break;
      case 'password':
        setPassword(value);
        break;
      case 'confirmPassword':
        setConfirmPassword(value);
        break;
      default:
        break;
    }
  };

export default handleChange;